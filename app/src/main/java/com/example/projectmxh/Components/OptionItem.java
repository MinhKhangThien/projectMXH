package com.example.projectmxh.Components;

public class OptionItem {
    private int icon;
    private String title;

    public OptionItem(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }
}
